
public class Ex01 {
	String userId;
	String userEmail;
	String userName;
	public Ex01(String userId, String userEmail, String userName) {
		this.userId = userId;
		this.userEmail = userEmail;
		this.userName = userName;
	}
	
	
	
	public String getUserId() {
		return userId;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public String getUserName() {
		return userName;
	}
	
	
	
}
